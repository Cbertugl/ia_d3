class Square:

  # ================================================================================================
  # CONSTANTS
  # ================================================================================================
  EMPTY = " "
  PLAYER = "X"
  EXIT = "E"
  WIND = "W"
  CREVASSE = "C"
  MONSTER_POOP = "P"
  MONSTER = "M"


  # ================================================================================================
  # CONSTRUCTOR
  # ================================================================================================
  def __init__(self, initialValue = EMPTY):
    self.__value = initialValue


  # ================================================================================================
  # PUBLIC METHODS
  # ================================================================================================
  def hasValue(self):
    return self.__value != self.EMPTY

  def getValue(self):
    return self.__value

  def setValue(self, value):
    self.__value = value